package com.example.hibarking.data_class;

public class DriverMechanicalRegister {
    String id, name, date;

    public DriverMechanicalRegister(String id, String name, String date, String description) {
        this.id = id;
        this.name = name;
        this.date = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

}
